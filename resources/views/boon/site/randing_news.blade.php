													<P>이처럼 김씨의 놀라운 적중률과 그 덕을 본 성공사례들이 입소문을 타자 리치투자증권은 현재 하루에 수백 명의 사람들이 무료회원가입을 할 정도로 무서운 속도로 성장하고 있다. 리치투자정보의 엄청난 고속성장은 주식시장의 세대교체를 예고하듯 대한민국 증권가를 뒤흔들고 있다.
													</P><br />
													<p>리치투자정보 측에서는 그가 추천한 종목들을 한곳에 모아 도표로 작성해놓았고, 잠재적 회원을 포함해 국내 직접투자를 하는 약 100만 명이 지켜볼 수 있어 신뢰성이 우수한 것으로 알려져 있다. <b><u>또한 업체에서는 투자 후 손실이 발생하게 될 경우 회비 전액(100%)을 환불해주는 VIP 서비스도 제공하고 있다.</u></b>
